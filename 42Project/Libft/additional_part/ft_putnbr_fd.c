#include "ft_lib.h"

void		ft_putnbr_fd(int n, int fd)
{
	int		sign;
	char	c;

	sign = 1;
	if (n < 0)
	{
		sign = -1;
		write(fd, "-", 1);
	}
	if (n >= 10 || n <= -10)
		ft_putnbr(sign * (n / 10));
	c = (sign * (n % 10)) + '0';
	write(fd, &c, 1);
}
