int	ft_str_is_alpha(char *str)
{
	unsigned int	i;

	i = 0;
	if (!(*str))
		return (1);
	while (*(str + i))
	{
		if (!((*(str + i) <= 'Z' && *(str + i) >= 'A') ||
				(*(str + i) <= 'z' && *(str + i) >= 'a')))
			return (0);
		i++;
	}
	return (1);
}
