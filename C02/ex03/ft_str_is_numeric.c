int	ft_str_is_numeric(char *str)
{
	unsigned int	i;

	i = 0;
	if (!(*str))
		return (1);
	while (*(str + i))
	{
		if (!(*(str + i) >= '1' && *(str + i) <= '9'))
			return (0);
		i++;
	}
	return (1);
}
